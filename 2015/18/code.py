def adjacent(grid, x, y):
    return len(filter(lambda c: c, [grid[(x-1, y-1)], grid[(x, y-1)], grid[(x+1, y-1)], grid[(x-1, y)], grid[(x+1, y)], grid[(x-1, y+1)], grid[(x, y+1)], grid[(x+1, y+1)]]))


def stick_corners(grid):
    grid.update({(x, y): True for x, y in product([0, GRID_SIZE-1], [0, GRID_SIZE-1])})


def step(grid):
    new_grid = defaultdict(bool)
    for x, y in product(xrange(GRID_SIZE), xrange(GRID_SIZE)):
        if grid[(x,y)] and adjacent(grid, x, y) in [2,3]:
            new_grid[(x,y)] = True
        elif grid[(x,y)] is False and adjacent(grid, x, y) == 3:
            new_grid[(x,y)] = True
    stick_corners(new_grid)
    return new_grid

