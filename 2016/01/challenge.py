def parse_input(input):
    for instruction in input.split(', '):
        yield instruction[0], int(instruction[1:])

TURN_RESULTS = {
    'N': {'L': 'W', 'R': 'E'},
    'S': {'L': 'E', 'R': 'W'},
    'W': {'L': 'S', 'R': 'N'},
    'E': {'L': 'N', 'R': 'S'},
}

x = 0
y = 0
d = 'N'

for turn, walk in parse_input(INPUT):
    d = TURN_RESULTS[d][turn]
    if d == 'N':
        y += walk
    elif d == 'S':
        y -= walk
    elif d == 'E':
        x += walk
    elif d == 'W':
        x -= walk

